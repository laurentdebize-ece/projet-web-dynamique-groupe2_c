function UnFiltre() {
    var form = document.getElementById("FiltreUnAUN");
    form.style.display = form.style.display === "none" ? "block" : "none";
}

function PlusieursFiltres() {
    var form = document.getElementById("FiltrePlusieurs");
    form.style.display = form.style.display === "none" ? "block" : "none";
}
